    private void tbnextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnextActionPerformed
        // TODO add your handling code here:\   
        try {
            int intValue = Integer.parseInt(txtnip.getText());
            
            karyawan k = new karyawan();
            k.setStatus("Aktif");
            String statusnya = k.getStatus();

                String pilih = (String) cbjabatan.getSelectedItem();
                if (pilih == "manager") { 
                    manager m = new manager(intValue, txtnama.getText(),txtemail.getText(),txttelp.getText(), 2, 500000.0, 3000000.0, statusnya);
                    double gajimanager = m.hitungGaji(); 

                    m.setjamKerja(10, true);
                    int durasi = m.getjamKerja();

                    m.nip_karyawan = intValue;
                    m.nama_karyawan = txtnama.getText();
                    m.email_karyawan = txtemail.getText();
                    m.nomor_telpon = txttelp.getText();
                    m.gaji = gajimanager;
                    m.status = statusnya;
                    m.durasi_kerja = durasi;

                    if (m.create()) {
                        JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan!");
                        show_data();
                        kosongkanform();
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
                    }

                }else if (pilih == "staff") {
                    staff s = new staff(intValue, txtnama.getText(),txtemail.getText(),txttelp.getText(), 3000000, statusnya);
                    double gajistaff = s.hitungGaji();

                    s.setjamKerja(8);
                    int durasi = s.getjamKerja();

                    s.nip_karyawan = intValue;
                    s.nama_karyawan = txtnama.getText();
                    s.email_karyawan = txtemail.getText();
                    s.nomor_telpon = txttelp.getText();
                    s.gaji = gajistaff;
                    s.durasi_kerja = durasi;


                    if (s.create()) {
                        JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan!");
                        show_data();
                        kosongkanform();
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
                    }

                }else if (pilih == "staff magang") {
                    staff_magang sm = new staff_magang(intValue, txtnama.getText(),txtemail.getText(),txttelp.getText(),2, 3000000, statusnya);
                    double gajimagang = sm.hitungGaji();

                    sm.setjamKerja(3, true, 2);
                    int durasi = sm.getjamKerja();

                    sm.nip_karyawan = intValue;
                    sm.nama_karyawan = txtnama.getText();
                    sm.email_karyawan = txtemail.getText();
                    sm.nomor_telpon = txttelp.getText();
                    sm.gaji = gajimagang;
                    sm.durasi_kerja = durasi;

                    if (sm.create()) {
                        JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan!");
                        show_data();
                        kosongkanform();
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
                    }
                }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Periksa NIP Anda Kembali!");
            return;
        }
    }//GEN-LAST:event_tbnextActionPerformed

